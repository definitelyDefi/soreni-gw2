// Helper to convert "H:MM" time strings to minutes from midnight
function t(...times: string[]): number[] {
  return times
    .map(s => {
      const [h, m] = s.split(':').map(Number);
      return h * 60 + m;
    })
    .sort((a, b) => a - b);
}

